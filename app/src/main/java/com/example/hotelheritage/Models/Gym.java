package com.example.hotelheritage.Models;

public class Gym {

    public String UserID;
    public String Name;
    public String Time;
    public String Date;
    public String TimeID;

    public Gym() {
    }

    public Gym(String userID, String name, String time, String date, String timeID) {
        UserID = userID;
        Name = name;
        Time = time;
        Date = date;
        TimeID = timeID;
    }
}
